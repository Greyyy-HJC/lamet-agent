"""Evaluation stage for extracting derived observables from cross-family comparisons.

Supported evaluation methods (selected via ``stage_parameters.evaluation.method``):

* ``cs_kernel_momentum_ratio``: extract the Collins-Soper kernel from the
  logarithmic ratio of x-space quasi distributions at different momenta.
  Optionally reduce the x-dependent kernel to a constant-in-x value per b.

* ``cs_kernel_momentum_fit``: extract the Collins-Soper kernel by simultaneously
  fitting all available momenta at each (x, b) point using the perturbative
  hard-matching kernel ``H(x, P^z)``.  The model is::

      quasi(x, P^z) = A(x) * H(x, P^z; mu) * (P^z / P^z_ref)^gamma(b)

  where ``H`` is ``CG_tmdwf_kernel_RGR`` and ``gamma(b)`` is the CS kernel.
  For each b, the x-dependent ``gamma(x, b)`` values are reduced to a single
  ``gamma(b)`` via a correlated constant fit.  A literature-comparison plot
  can be generated by supplying ``literature_data_path`` (a gvar ``.gv`` file).
"""

from __future__ import annotations

import itertools
import logging
from pathlib import Path
from typing import Any

import numpy as np

from lamet_agent.artifacts import ArtifactRecord, StageResult
from lamet_agent.extensions.kernels import CG_tmdwf_kernel_RGR
from lamet_agent.extensions.statistics import (
    bootstrap_average,
    constant_fit,
    gv,
    jackknife_average,
)
from lamet_agent.extensions.plot_presets import AXIS_FONT, SMALL_AXIS_FONT, default_plot
from lamet_agent.plotting import save_series_collection_plot
from lamet_agent.stages.base import StageContext
from lamet_agent.stages.registry import register_stage
from lamet_agent.utils import ensure_directory, write_columnar_data, write_json

logger = logging.getLogger(__name__)

try:
    import lsqfit as _lsqfit
except ModuleNotFoundError:  # pragma: no cover
    _lsqfit = None

try:
    import matplotlib.pyplot as plt
    import matplotlib
    matplotlib.use("Agg")
except ModuleNotFoundError:  # pragma: no cover
    plt = None


@register_stage
class EvaluationStage:
    """Extract derived observables that require cross-family aggregation."""

    name = "evaluation"
    description = "Extract observables such as the Collins-Soper kernel from cross-momentum comparisons."

    def run(self, context: StageContext) -> StageResult:
        stage_dir = ensure_directory(context.stage_directory(self.name))
        parameters = self._resolve_parameters(context)
        method = parameters["method"]

        if method == "cs_kernel_momentum_ratio":
            return self._run_cs_kernel_momentum_ratio(context, stage_dir, parameters)
        if method == "cs_kernel_momentum_fit":
            return self._run_cs_kernel_momentum_fit(context, stage_dir, parameters)

        raise ValueError(f"Unsupported evaluation method: {method!r}")

    # ------------------------------------------------------------------
    # Parameter resolution
    # ------------------------------------------------------------------

    @staticmethod
    def _resolve_parameters(context: StageContext) -> dict[str, Any]:
        params = dict(context.parameters_for("evaluation"))
        x_window = params.get("x_window", [0.3, 0.7])
        plot_ylim = params.get("plot_ylim", [-3.0, 3.0])
        return {
            "method": str(params.get("method", "cs_kernel_momentum_ratio")),
            "x_window": [float(x_window[0]), float(x_window[1])],
            "constant_x_fit": bool(params.get("constant_x_fit", True)),
            "momentum_pairs": params.get("momentum_pairs", "all"),
            "family_selector": dict(params.get("family_selector", {})),
            "plot_ylim": [float(plot_ylim[0]), float(plot_ylim[1])],
            # cs_kernel_momentum_fit specific
            "fix_pz_gev": float(params.get("fix_pz_gev", 1.62)),
            "mu_renorm": float(params.get("mu_renorm", 2.0)),
            "csk_prior_mean": float(params.get("csk_prior_mean", -0.5)),
            "csk_prior_sigma": float(params.get("csk_prior_sigma", 1.0)),
            "literature_data_path": params.get("literature_data_path", None),
        }

    # ------------------------------------------------------------------
    # CS kernel via momentum ratio
    # ------------------------------------------------------------------

    def _run_cs_kernel_momentum_ratio(
        self,
        context: StageContext,
        stage_dir: Path,
        parameters: dict[str, Any],
    ) -> StageResult:
        previous = context.stage_payloads.get("fourier_transform", {})
        families = previous.get("_transformed_families")
        if not families:
            raise ValueError("evaluation requires _transformed_families from the fourier_transform stage.")

        families = self._select_families(families, parameters["family_selector"])
        grouped = self._group_families_by_b(families)

        x_window = parameters["x_window"]
        do_constant_fit = parameters["constant_x_fit"]

        cs_kernel_raw: dict[str, Any] = {}
        cs_kernel_reduced: list[dict[str, Any]] = []
        evaluation_results: list[dict[str, Any]] = []
        artifacts: list[ArtifactRecord] = []

        for (group_key, b_value), b_families in sorted(grouped.items()):
            momenta, momentum_labels, total_momenta_gev = self._extract_momentum_info(b_families)
            if len(b_families) < 2:
                logger.warning("evaluation: b=%d has only %d momentum; skipping CS kernel extraction.", b_value, len(b_families))
                continue

            pairs = self._build_momentum_pairs(b_families, parameters["momentum_pairs"])
            pair_results: list[dict[str, Any]] = []
            for idx1, idx2 in pairs:
                family1 = b_families[idx1]
                family2 = b_families[idx2]
                result = self._cs_kernel_for_pair(family1, family2, x_window, do_constant_fit)
                pair_label = f"p{momentum_labels[idx2]}_p{momentum_labels[idx1]}"
                result["pair_label"] = pair_label
                result["b"] = b_value
                pair_results.append(result)
                cs_kernel_raw[f"b{b_value}_{pair_label}"] = {
                    "x_axis": result["x_axis"].tolist(),
                    "mean": result["mean"].tolist(),
                    "error": result["error"].tolist(),
                }

            if do_constant_fit and pair_results:
                reduced = self._reduce_across_pairs(pair_results, b_value, group_key)
                cs_kernel_reduced.append(reduced)

            evaluation_results.extend(pair_results)
            artifacts.extend(self._write_b_artifacts(stage_dir, context, b_value, pair_results, group_key, x_window, parameters["plot_ylim"]))

        if cs_kernel_reduced:
            artifacts.extend(self._write_reduced_artifacts(stage_dir, context, cs_kernel_reduced))

        payload: dict[str, Any] = {
            "method": "cs_kernel_momentum_ratio",
            "cs_kernel_raw": cs_kernel_raw,
            "cs_kernel_reduced": [
                {k: v for k, v in entry.items() if not str(k).startswith("_")}
                for entry in cs_kernel_reduced
            ],
            "_evaluation_results": evaluation_results,
            "_cs_kernel_reduced": cs_kernel_reduced,
        }
        n_pairs = len(evaluation_results)
        n_b = len(cs_kernel_reduced)
        summary = (
            f"Extracted Collins-Soper kernel via momentum ratio for {n_pairs} momentum pair(s) "
            f"across {n_b} b value(s)."
        )
        return StageResult(stage_name=self.name, summary=summary, payload=payload, artifacts=artifacts)

    # ------------------------------------------------------------------
    # CS kernel via multi-momentum fit (new method)
    # ------------------------------------------------------------------

    def _run_cs_kernel_momentum_fit(
        self,
        context: StageContext,
        stage_dir: Path,
        parameters: dict[str, Any],
    ) -> StageResult:
        """Fit all momenta simultaneously at each (x, b) to extract gamma(b).

        The model at each x point is::

            quasi(P^z) = A * H(x, P^z; mu) * (P^z / P^z_ref)^gamma

        where H is the CG_tmdwf_kernel_RGR and gamma is the CS kernel.
        Correlations across P^z values (arising from shared gauge configs)
        are preserved via a joint jackknife/bootstrap average.
        """
        if gv is None:
            raise RuntimeError("gvar is required for cs_kernel_momentum_fit.")
        if _lsqfit is None:
            raise RuntimeError("lsqfit is required for cs_kernel_momentum_fit.")

        previous = context.stage_payloads.get("fourier_transform", {})
        families = previous.get("_transformed_families")
        if not families:
            raise ValueError("evaluation requires _transformed_families from the fourier_transform stage.")

        families = self._select_families(families, parameters["family_selector"])
        grouped = self._group_families_by_b(families)

        x_window = parameters["x_window"]
        fix_pz_gev = parameters["fix_pz_gev"]
        mu_renorm = parameters["mu_renorm"]
        prior_A_mean = 1.0
        prior_A_sigma = 10.0
        prior_csk_mean = parameters["csk_prior_mean"]
        prior_csk_sigma = parameters["csk_prior_sigma"]

        cs_kernel_raw: dict[str, Any] = {}
        cs_kernel_reduced: list[dict[str, Any]] = []
        evaluation_results: list[dict[str, Any]] = []
        artifacts: list[ArtifactRecord] = []

        for (group_key, b_value), b_families in sorted(grouped.items()):
            if len(b_families) < 2:
                logger.warning(
                    "evaluation: b=%d has only %d momentum; skipping CS kernel momentum fit.",
                    b_value, len(b_families),
                )
                continue

            # Sort families by increasing P^z
            b_families = sorted(b_families, key=lambda f: float(f["momentum"]["total_gev"]))
            pz_gev_arr = np.array([float(f["momentum"]["total_gev"]) for f in b_families])
            x_axis = np.asarray(b_families[0]["x_axis"], dtype=float)

            resampling_method = str(b_families[0]["metadata"].get("resampling_method", "jackknife"))
            is_jackknife = resampling_method == "jackknife"

            # Compute x-dependent CS kernel via per-x multi-momentum fit
            csk_xdep: list = []  # list of gvar values at each x
            n_x = len(x_axis)

            for x_idx, x_val in enumerate(x_axis):
                # Stack samples at this x across all pz: shape (n_samp, n_pz)
                sample_cols = []
                for fam in b_families:
                    col = np.asarray(fam["real_samples"], dtype=float)[:, x_idx]
                    sample_cols.append(col)
                n_samp = min(col.shape[0] for col in sample_cols)
                stacked = np.stack([col[:n_samp] for col in sample_cols], axis=1)

                # Correlated gvar average preserving pz correlations
                if is_jackknife:
                    quasi_gv = jackknife_average(stacked, axis=0)  # shape (n_pz,)
                else:
                    quasi_gv = bootstrap_average(stacked, axis=0)

                # Pre-compute hard kernels (deterministic, no gvar)
                hard_kernels = np.array([
                    float(CG_tmdwf_kernel_RGR(x_val, pz, mu=mu_renorm))
                    for pz in pz_gev_arr
                ])

                log_pz_ratio = np.log(pz_gev_arr / fix_pz_gev)

                def _make_model(hk, logr):
                    def fcn(pz_dummy, p):
                        exp_term = np.exp(logr * p["csk"])
                        return p["A"] * hk * exp_term
                    return fcn

                priors = gv.BufferDict({
                    "A": gv.gvar(prior_A_mean, prior_A_sigma),
                    "csk": gv.gvar(prior_csk_mean, prior_csk_sigma),
                })

                try:
                    fit = _lsqfit.nonlinear_fit(
                        data=(pz_gev_arr, quasi_gv),
                        prior=priors,
                        fcn=_make_model(hard_kernels, log_pz_ratio),
                        maxit=10_000,
                        svdcut=1e-4,
                    )
                    if fit.Q < 0.05:
                        logger.debug(
                            "evaluation: momentum fit b=%d x=%.3f Q=%.4f (low quality).",
                            b_value, x_val, fit.Q,
                        )
                    csk_xdep.append(fit.p["csk"])
                except Exception as exc:
                    logger.warning("evaluation: fit failed at b=%d x=%.3f: %s", b_value, x_val, exc)
                    csk_xdep.append(gv.gvar(0.0, 1e6))

            # Store x-dependent result
            csk_mean = np.array([gv.mean(v) for v in csk_xdep])
            csk_error = np.array([gv.sdev(v) for v in csk_xdep])
            cs_kernel_raw[f"b{b_value}"] = {
                "x_axis": x_axis.tolist(),
                "mean": csk_mean.tolist(),
                "error": csk_error.tolist(),
            }

            pair_result: dict[str, Any] = {
                "b": b_value,
                "pair_label": f"fit_{len(b_families)}pz",
                "x_axis": x_axis,
                "mean": csk_mean,
                "error": csk_error,
                "_csk_xdep_gv": csk_xdep,
            }
            evaluation_results.append(pair_result)
            artifacts.extend(
                self._write_b_artifacts(
                    stage_dir, context, b_value, [pair_result], group_key,
                    x_window, parameters["plot_ylim"],
                )
            )

            # Constant fit in x window
            mask = (x_axis >= x_window[0]) & (x_axis <= x_window[1])
            csk_in_window = [csk_xdep[i] for i in range(n_x) if mask[i]]
            if csk_in_window:
                try:
                    csk_constant = constant_fit(
                        csk_in_window,
                        prior_mean=prior_csk_mean,
                        prior_sigma=prior_csk_sigma * 2,
                    )
                    stat_err = float(gv.sdev(csk_constant))
                    # Systematic: std of mean values across the x window,
                    # capturing residual x-dependence.  Using std (not range)
                    # is consistent with the cs_kernel_sys convention in the
                    # reference analysis (soft_function_mpi300/cg_cs_kernel.ipynb)
                    # where per-sample std is averaged as the systematic.
                    means_in_window = np.array([gv.mean(v) for v in csk_in_window])
                    valid = np.isfinite(means_in_window)
                    sys_err = float(np.std(means_in_window[valid])) if valid.sum() > 1 else 0.0
                    total_err = float(np.sqrt(stat_err**2 + sys_err**2))
                    reduced: dict[str, Any] = {
                        "b": b_value,
                        "group_key": list(group_key),
                        "value": float(gv.mean(csk_constant)),
                        "stat_error": stat_err,
                        "sys_error": sys_err,
                        "total_error": total_err,
                        "n_momenta": len(b_families),
                        "n_x_points": int(np.sum(mask)),
                        "_gv": csk_constant,
                    }
                    cs_kernel_reduced.append(reduced)
                except Exception as exc:
                    logger.warning("evaluation: constant x-fit failed for b=%d: %s", b_value, exc)
            else:
                logger.warning("evaluation: no x points in window for b=%d, x_window=%s.", b_value, x_window)

        if cs_kernel_reduced:
            artifacts.extend(self._write_reduced_artifacts(stage_dir, context, cs_kernel_reduced))

            # Optional literature comparison plot
            lit_path_raw = parameters.get("literature_data_path")
            if lit_path_raw:
                lit_path = Path(lit_path_raw)
                if not lit_path.is_absolute():
                    # Resolve relative to the manifest directory
                    manifest_dir = (
                        Path(context.manifest.manifest_path).parent
                        if getattr(context.manifest, "manifest_path", None)
                        else Path.cwd()
                    )
                    lit_path = (manifest_dir / lit_path).resolve()
                if lit_path.exists():
                    try:
                        lit_artifacts = self._write_literature_comparison_plot(
                            stage_dir, context, cs_kernel_reduced, lit_path,
                        )
                        artifacts.extend(lit_artifacts)
                    except Exception as exc:
                        logger.warning("evaluation: literature comparison plot failed: %s", exc)
                else:
                    logger.warning("evaluation: literature data file not found: %s", lit_path)

        payload: dict[str, Any] = {
            "method": "cs_kernel_momentum_fit",
            "cs_kernel_raw": cs_kernel_raw,
            "cs_kernel_reduced": [
                {k: v for k, v in entry.items() if not str(k).startswith("_")}
                for entry in cs_kernel_reduced
            ],
            "_evaluation_results": evaluation_results,
            "_cs_kernel_reduced": cs_kernel_reduced,
        }
        n_b = len(cs_kernel_reduced)
        n_pz_total = sum(r.get("n_momenta", 0) for r in cs_kernel_reduced)
        summary = (
            f"Extracted Collins-Soper kernel via multi-momentum fit for {n_b} b value(s) "
            f"using up to {n_pz_total // max(n_b, 1)} momentum/momenta per b."
        )
        return StageResult(stage_name=self.name, summary=summary, payload=payload, artifacts=artifacts)

    # ------------------------------------------------------------------
    # Literature comparison plot
    # ------------------------------------------------------------------

    def _write_literature_comparison_plot(
        self,
        stage_dir: Path,
        context: StageContext,
        cs_kernel_reduced: list[dict[str, Any]],
        lit_path: Path,
    ) -> list[ArtifactRecord]:
        """Create a comparison plot of this-work CS kernel against literature data."""
        if plt is None or gv is None:
            logger.warning("evaluation: matplotlib or gvar unavailable; skipping literature comparison.")
            return []

        try:
            csk_others = gv.load(str(lit_path))
        except Exception as exc:
            logger.warning("evaluation: could not load literature data from %s: %s", lit_path, exc)
            return []

        # Determine lattice spacing (fm) from setup metadata if available
        # Used to convert b (lattice units) -> b_perp (fm)
        a_fm: float | None = None
        try:
            setup_ids = list(context.manifest.metadata.get("setups", {}).keys())
            if setup_ids:
                setup = context.manifest.setup_metadata(setup_ids[0])
                a_fm = float(setup.get("lattice_spacing_fm", 0.0)) or None
        except Exception:
            pass

        b_arr = np.array([float(r["b"]) for r in cs_kernel_reduced])
        b_phys = b_arr * a_fm if a_fm is not None else b_arr
        csk_stat = np.array([float(r["value"]) for r in cs_kernel_reduced])
        csk_stat_err = np.array([float(r["stat_error"]) for r in cs_kernel_reduced])
        xlabel = r"$b_\perp$ [fm]" if a_fm is not None else r"$b_\perp$ [lattice units]"

        artifacts: list[ArtifactRecord] = []
        for plot_format in context.manifest.outputs.plot_formats:
            plot_path = stage_dir / f"cs_kernel_comparison.{plot_format}"
            try:
                self._render_comparison_plot(
                    csk_others, b_phys, csk_stat, csk_stat_err, xlabel, plot_path,
                )
                artifacts.append(ArtifactRecord(
                    name=f"cs_kernel_comparison_{plot_format}",
                    kind="plot",
                    path=plot_path,
                    description="Collins-Soper kernel comparison with literature.",
                    format=plot_format,
                ))
            except Exception as exc:
                logger.warning("evaluation: failed to render comparison plot: %s", exc)
        return artifacts

    @staticmethod
    def _render_comparison_plot(
        csk_others: dict,
        b_phys: np.ndarray,
        csk_stat: np.ndarray,
        csk_stat_err: np.ndarray,
        xlabel: str,
        plot_path: Path,
    ) -> None:
        """Render and save the literature comparison figure."""
        fig, ax = default_plot()

        # ---- Literature bands and curves ---- #
        def _fill(key, label, alpha=0.35, color=None):
            if key not in csk_others:
                return
            d = csk_others[key]
            b = np.asarray(d["b_array"])
            mu = np.asarray(d["csk_mean"])
            sg = np.asarray(d["csk_sdev"])
            kw = {"alpha": alpha, "label": label}
            if color is not None:
                kw["color"] = color
            ax.fill_between(b, mu - sg, mu + sg, **kw)

        def _errbar(key, label, fmt="o", color=None, offset=0.0):
            if key not in csk_others:
                return
            d = csk_others[key]
            b = np.asarray(d["b_array"]) + offset
            mu = np.asarray(d["csk_mean"])
            sg = np.asarray(d["csk_sdev"])
            kw = {"fmt": fmt, "label": label, "capsize": 3,
                  "elinewidth": 1.2, "markersize": 5}
            if color is not None:
                kw["color"] = color
            ax.errorbar(b, mu, sg, **kw)

        def _line(key, label, ls="--", color="black"):
            if key not in csk_others:
                return
            d = csk_others[key]
            ax.plot(np.asarray(d["b_array"]), np.asarray(d["csk"]),
                    label=label, color=color, linestyle=ls)

        _line("N3LO", r"$\rm N^3LO$", ls="--")
        _line("N3LL", r"$\rm N^3LL$", ls=":")
        _fill("MAPNN25", "MAPNN25", alpha=0.30)
        _fill("MAP22", "MAP22", alpha=0.55)
        _fill("ART23", "ART23", alpha=0.40)
        _fill("ASWZ24", "ASWZ24", alpha=0.40)
        _fill("MAP24", "MAP24FI", alpha=0.40)
        _fill("ART25", "ART25", alpha=0.40)
        _errbar("LPC23", "LPC23", fmt="s", offset=0.0)
        _errbar("DWF24", "DWF24", fmt="D", offset=-0.005)

        # ---- This work ---- #
        ax.errorbar(
            b_phys, csk_stat, csk_stat_err,
            fmt="o", color="red", label="This Work",
            elinewidth=2, markersize=7, capsize=3, zorder=10,
        )

        ax.set_xlabel(xlabel, **AXIS_FONT)
        ax.set_ylabel(r"$\gamma^{\overline{\mathrm{MS}}}(b_\perp;\,\mu)$", **AXIS_FONT)
        ax.set_ylim([-3, 2])
        ax.legend(ncol=4, fontsize=11, loc="upper right")
        fig.tight_layout()
        fig.savefig(str(plot_path), transparent=True)
        plt.close(fig)

    # ------------------------------------------------------------------
    # Family grouping and selection
    # ------------------------------------------------------------------

    @staticmethod
    def _select_families(families: list[dict[str, Any]], selector: dict[str, Any]) -> list[dict[str, Any]]:
        if not selector:
            return list(families)
        return [
            f for f in families
            if all(f["metadata"].get(k) == v for k, v in selector.items())
        ]

    @staticmethod
    def _group_families_by_b(
        families: list[dict[str, Any]],
    ) -> dict[tuple[tuple, int], list[dict[str, Any]]]:
        """Group families by (group_key, b) where group_key = (setup_id, gamma, flavor, smearing)."""
        grouped: dict[tuple[tuple, int], list[dict[str, Any]]] = {}
        for family in families:
            meta = family["metadata"]
            group_key = (
                str(meta["setup_id"]),
                str(meta["gamma"]),
                str(meta.get("flavor", "")),
                str(meta["smearing"]),
            )
            b = int(meta["b"])
            key = (group_key, b)
            grouped.setdefault(key, []).append(family)
        for key in grouped:
            grouped[key].sort(key=lambda f: sum(int(c) ** 2 for c in f["metadata"]["momentum"]))
        return grouped

    @staticmethod
    def _extract_momentum_info(
        b_families: list[dict[str, Any]],
    ) -> tuple[list[tuple[int, ...]], list[str], list[float]]:
        momenta = []
        labels = []
        totals_gev = []
        for f in b_families:
            meta = f["metadata"]
            mom = (int(meta["px"]), int(meta["py"]), int(meta["pz"]))
            momenta.append(mom)
            labels.append(f"{mom[0]}{mom[1]}{mom[2]}")
            mom_info = f.get("momentum", {})
            total_gev = float(mom_info.get("total_gev", 0.0))
            if total_gev <= 0.0:
                total_gev = float(np.sqrt(sum(c ** 2 for c in mom)))
            totals_gev.append(total_gev)
        return momenta, labels, totals_gev

    @staticmethod
    def _build_momentum_pairs(
        b_families: list[dict[str, Any]],
        spec: Any,
    ) -> list[tuple[int, int]]:
        n = len(b_families)
        if spec == "all" or spec is None:
            return [(i, j) for i, j in itertools.combinations(range(n), 2)]
        return [(int(p[0]), int(p[1])) for p in spec]

    # ------------------------------------------------------------------
    # CS kernel computation (momentum-ratio method)
    # ------------------------------------------------------------------

    def _cs_kernel_for_pair(
        self,
        family_lo: dict[str, Any],
        family_hi: dict[str, Any],
        x_window: list[float],
        do_constant_fit: bool,
    ) -> dict[str, Any]:
        """Compute the CS kernel from one momentum pair using sample-level data."""
        x_axis_lo = np.asarray(family_lo["x_axis"], dtype=float)
        x_axis_hi = np.asarray(family_hi["x_axis"], dtype=float)
        if not np.allclose(x_axis_lo, x_axis_hi):
            raise ValueError("evaluation: x grids for momentum pair do not match.")
        x_axis = x_axis_lo

        real_lo = np.asarray(family_lo["real_samples"], dtype=float)
        real_hi = np.asarray(family_hi["real_samples"], dtype=float)

        mom_lo = family_lo.get("momentum", {})
        mom_hi = family_hi.get("momentum", {})
        p_lo = float(mom_lo.get("total_gev", 0.0))
        p_hi = float(mom_hi.get("total_gev", 0.0))
        if p_lo <= 0 or p_hi <= 0:
            meta_lo = family_lo["metadata"]
            meta_hi = family_hi["metadata"]
            p_lo = float(np.sqrt(int(meta_lo["px"]) ** 2 + int(meta_lo["py"]) ** 2 + int(meta_lo["pz"]) ** 2))
            p_hi = float(np.sqrt(int(meta_hi["px"]) ** 2 + int(meta_hi["py"]) ** 2 + int(meta_hi["pz"]) ** 2))

        ln_p_ratio = float(np.log(p_hi / p_lo)) if p_hi > p_lo > 0 else 1.0

        n_samples = min(real_lo.shape[0], real_hi.shape[0])
        cs_samples = np.full((n_samples, len(x_axis)), np.nan)
        for n in range(n_samples):
            with np.errstate(divide="ignore", invalid="ignore"):
                ratio = real_hi[n] / real_lo[n]
                cs_samples[n] = np.log(ratio) / ln_p_ratio

        method = str(family_lo["metadata"].get("resampling_method", "jackknife"))
        cs_mean = np.nanmean(cs_samples, axis=0)
        if method == "jackknife":
            cs_error = np.nanstd(cs_samples, axis=0, ddof=1) * np.sqrt(max(n_samples - 1, 1))
        else:
            cs_error = np.nanstd(cs_samples, axis=0, ddof=1)

        result: dict[str, Any] = {
            "x_axis": x_axis,
            "mean": cs_mean,
            "error": cs_error,
            "samples": cs_samples,
            "p_lo_gev": p_lo,
            "p_hi_gev": p_hi,
            "ln_p_ratio": ln_p_ratio,
        }

        if do_constant_fit and gv is not None:
            result["constant_fit"] = self._constant_x_fit(x_axis, cs_samples, x_window, method)

        return result

    @staticmethod
    def _constant_x_fit(
        x_axis: np.ndarray,
        cs_samples: np.ndarray,
        x_window: list[float],
        resampling_method: str,
    ) -> dict[str, Any]:
        """Fit the CS kernel to a constant within the x window for each sample."""
        mask = (x_axis >= x_window[0]) & (x_axis <= x_window[1])
        x_selected = x_axis[mask]
        if len(x_selected) == 0:
            return {"value": 0.0, "error": 0.0, "x_window": x_window, "n_points": 0}

        cs_in_window = cs_samples[:, mask]
        mean_per_x = np.nanmean(cs_in_window, axis=0)
        if resampling_method == "jackknife":
            n = cs_in_window.shape[0]
            error_per_x = np.nanstd(cs_in_window, axis=0, ddof=1) * np.sqrt(max(n - 1, 1))
        else:
            error_per_x = np.nanstd(cs_in_window, axis=0, ddof=1)

        valid = np.isfinite(mean_per_x) & np.isfinite(error_per_x) & (error_per_x > 0)
        if not np.any(valid):
            return {"value": 0.0, "error": 0.0, "x_window": x_window, "n_points": 0}

        gv_values = gv.gvar(mean_per_x[valid], np.clip(error_per_x[valid], 1e-12, None))
        try:
            fit_val = constant_fit(gv_values)
            return {
                "value": float(gv.mean(fit_val)),
                "error": float(gv.sdev(fit_val)),
                "x_window": x_window,
                "n_points": int(np.sum(valid)),
            }
        except Exception:
            logger.warning("evaluation: constant_fit failed in x window; falling back to weighted mean.")
            weights = 1.0 / error_per_x[valid] ** 2
            wmean = float(np.average(mean_per_x[valid], weights=weights))
            werr = float(1.0 / np.sqrt(np.sum(weights)))
            return {"value": wmean, "error": werr, "x_window": x_window, "n_points": int(np.sum(valid))}

    # ------------------------------------------------------------------
    # Cross-pair reduction
    # ------------------------------------------------------------------

    @staticmethod
    def _reduce_across_pairs(
        pair_results: list[dict[str, Any]],
        b_value: int,
        group_key: tuple,
    ) -> dict[str, Any]:
        """Combine constant-fit results from multiple momentum pairs into a single value with systematic."""
        values = []
        errors = []
        for pr in pair_results:
            cf = pr.get("constant_fit", {})
            if cf.get("n_points", 0) > 0:
                values.append(cf["value"])
                errors.append(cf["error"])

        if not values:
            return {"b": b_value, "group_key": list(group_key), "value": 0.0, "stat_error": 0.0, "sys_error": 0.0, "total_error": 0.0, "n_pairs": 0}

        values_arr = np.array(values)
        errors_arr = np.array(errors)
        weights = 1.0 / errors_arr ** 2
        central = float(np.average(values_arr, weights=weights))
        stat = float(1.0 / np.sqrt(np.sum(weights)))
        sys = float(np.std(values_arr, ddof=0)) if len(values_arr) > 1 else 0.0
        total = float(np.sqrt(stat ** 2 + sys ** 2))
        return {
            "b": b_value,
            "group_key": list(group_key),
            "value": central,
            "stat_error": stat,
            "sys_error": sys,
            "total_error": total,
            "n_pairs": len(values),
            "per_pair": [
                {"pair_label": pr["pair_label"], "value": pr["constant_fit"]["value"], "error": pr["constant_fit"]["error"]}
                for pr in pair_results if pr.get("constant_fit", {}).get("n_points", 0) > 0
            ],
        }

